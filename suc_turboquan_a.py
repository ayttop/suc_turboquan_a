model = turboquant.wrap(
    model,
    bit_width=3,              # أفضل توازن
    residual_length=128,      # آخر 128 توكن بدون ضغط
    n_outlier_channels=8,     # تحسين الجودة
    verbose=True              # يعرض stats
)

# inference
